#4.Assignment Operators: Used to assign
#                           - a value to a variable.(x=10)
#                           - a variable to another variable.(y=x)
#                           - an expression to a variable. (z=x+y-4)                         
#Assignment operators are also called as "Short-hand operators."
'''
Expression   Equivalence
x+=2           x=x+2
x-=2           x=x-2
x*=2           x=x*2
x/=2           x=x/2
x//=2          x=x//2
x%=2           x=x%2
x**=2          x=x**2
'''

x=10
x+=4
print(x)

x-=6
print(x)

x*=2
print(x)

x/=5
print(x)

x=10
x//=3
print(x)

x**=3
print(x)















