#while loop: It executes set of statements for repeated number of times or for multiple times.

#All the stmts within the while loop should follow the indentation.

#Syntax for defining while loop:

'''
initialization
while(condition):
    stmt1
    stmt2
    stmt3
    stmt4
    stmt5
    increment/decrement

The stmts within the while loop will be executed until the condition becomes false
Once the condition becomes false then ctrl comes out of while loop and executes
the other stmts.
'''

#To print numbers from 1 to 10:

x=1
while(x<=10):
    print(x)
    x=x+1
print("end")























