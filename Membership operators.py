#Membership Operators: Used to check whether an element is present or not.
#Membership Operators also returns either True or False.
#Membership Operators are:
#1.in
#2.not in

#ex1:
x=[10,20,30,40,50]
print(20 in x)
print(70 not in x)
print(70 in x)

#ex2:
y="python"
print('p' in y)
print('a' not in y)
print('a' in y)

#ex3:
emps=["Miller","Ajay","Blake","Rahul","James"]
print("Amar" in emps)
print("James" in emps)
print("miller" in emps) # python is case-sensitive
print("John" not in emps)
















    
