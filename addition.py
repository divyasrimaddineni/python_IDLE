#Accepting 2 values from the user keyboard and performing addition
#NOTE: Whatever we are accepting from the user keyboard by default taken as "String".
      # we need to perform "typecasting" to convert the required fields.
x=input("Enter value of x:")
y=input("Enter value of y:")
print(x,type(x))
print(y,type(y))
print(x+y)

x=int(input("Enter value of x:"))
y=int(input("Enter value of y:"))
print(x,type(x))
print(y,type(y))
print(x+y)

